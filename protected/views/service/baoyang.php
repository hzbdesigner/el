如何保养木质家具呢，给大家提供几点参考：
<br>
<br>
1、经常用软布顺着木的纹理，为家具去尘，去尘之前，应在软布上沾点喷剂，不要干布揩抹，以免擦花。
<br>
2、注意保持室内干燥，以免家具受潮霉变。   
<br>
3、实木家具在较干燥的环境下使用时，需采用人工加湿措施，如定期用软布沾水擦拭家具。
<br>
4、大衣柜、书橱等家具，其柜顶不要压放重物，不然柜门会出现凹凸形状，使门关不严
<br>
5、尽量避免让家具表面接触到腐蚀性液体。
<br>
6、定期打蜡，每隔6-12个月，用膏状蜡为家具上一层蜡。上蜡之前，应先用较温和的非碱性肥皂水将旧蜡抹除。
<br>
7、切忌放在强阳光下暴晒，也不能放在过分干燥处，以防木料豁裂变形。
<br>
8、尽可能用垫子垫在热盘子下，以免食物汤料外溢，沾污或损坏桌面。
